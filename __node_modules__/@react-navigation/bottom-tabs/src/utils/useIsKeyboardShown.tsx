import * as React from 'react';
import { Keyboard, Platform } from 'react-native';

export default function useIsKeyboardShown() {
  const [isKeyboardShown, setIsKeyboardShown] = React.useState(false);

  React.useEffect(() => {
    const handleKeyboardShow = () => setIsKeyboardShown(true);
    const handleKeyboardHide = () => setIsKeyboardShown(false);
    let subscriptionShow
    let subscriptionHide

    if (Platform.OS === 'ios') {
      subscriptionShow = Keyboard.addListener('keyboardWillShow', handleKeyboardShow);
      subscriptionHide = Keyboard.addListener('keyboardWillHide', handleKeyboardHide);
    } else {
      subscriptionShow = Keyboard.addListener('keyboardDidShow', handleKeyboardShow);
      subscriptionHide = Keyboard.addListener('keyboardDidHide', handleKeyboardHide);
    }

    return () => {
      subscriptionShow.remove()
      subscriptionHide.remove()
    };
  }, []);

  return isKeyboardShown;
}
